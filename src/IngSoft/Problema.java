package IngSoft;

public class Problema {

	private int nivelProblema;
	private int idProblema;

	
	public Problema(int nivelProblema, int idProblema) {
		super();
		this.nivelProblema = nivelProblema;
		this.idProblema = idProblema;
	}

	public int getNivelProblema() {
		return nivelProblema;
	}

	public void setNivelProblema(int nivelProblema) {
		this.nivelProblema = nivelProblema;
	}

	public int getIdProblema() {
		return idProblema;
	}

	public void setIdProblema(int idProblema) {
		this.idProblema = idProblema;
	}
	
	
}
